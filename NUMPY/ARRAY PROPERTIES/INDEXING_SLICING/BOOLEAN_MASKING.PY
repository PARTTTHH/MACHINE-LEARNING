# SORT OR FILTER DATA, 10X FASTER THAN LOOPS BASED ON CONDITION, IF ELEMENT GETS TRUE ON CONDITION THEN 
# ELEMENT GET INCLUDE IN NEW DATA OTHERWISE OR IF GET FALSE THEN ITS NOT INCLUDE IN OUR NEW DATA
# AND SKIPS THAT ELEMENT

