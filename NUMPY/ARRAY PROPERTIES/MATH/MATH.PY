# IF WE WANT TO PERFORM MATHEMATICAL OPERATIONS ON ARRAY NUM PY MAKES 
# MAKES IT VERY EASY DIRECT PERFOM NOT NEEDS OF LOOPS LIKE LIST 

import numpy as np

arr = np.array([10, 20, 30, 40, 50])

print(arr + 5)
print(arr - 5)
print(arr * 2)
print(arr / 5)
print(arr ** 2)
print(arr // 5)
print(arr % 5)
