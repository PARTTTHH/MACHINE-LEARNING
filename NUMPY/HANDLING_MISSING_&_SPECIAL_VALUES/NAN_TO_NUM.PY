"""
WE SEE WHY WE CAN FIND MISSING OR NOT A NUMBER VALUE LETS SEE HOW WE CAN REPLCE THAT VALUES WITH 
OUR REAL VALUES BUT BEFOR THAT WE NEED TO CONVERT THAT ARRAY INTO NUMS-ARRAY AGAIN FOR THAT WE HAVE 
AGAIN A BUIT-IN NUMPY FUNTION THIS FUNCTION CONVERTS ANF ALSO REPLCE VALUES FOR US
                        np.nan_to_num(arr, nan=, posinf=, neginf=)          
BY DEFAULT NAN VALUE GET 0 
IN ARGUMENT SECTION WE CAN SEE THE OPETON TO REPLACE NON VALUR INTO OUR REAL VALUES
FOR REPLACE A NAN VALUE U CAN PASS nan HERE
FOR REPLACE A POSSITIVE INFINITE VALUE U CAN PASS posinf HERE
FOR REPLACE A NEGATIVE INFINITE VALUE U CAN PASS neginf HERE
"""
import numpy as np

arr = np.array([1,2,3,np.nan,4,5,np.nan])

cleaner_arr = np.nan_to_num(arr, nan=100)       #WE JUST NAN REPLACE HERE

print(cleaner_arr)