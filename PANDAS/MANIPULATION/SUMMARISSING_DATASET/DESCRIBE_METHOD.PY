"""
-describe() IS A BUILT-IN METHOD IN PANDAS USED TO GET STATISTICAL SUMMARY OF DATASET

-SYNTAX : 
            DataFrame.describe()
IT HELPS IN --- 
1. TO UNDERSTAND DATA BEHAVIOR
2. FIND UNSUAL VALUES
3. MAKE DECISIONS BEFORE DATA CLEANING OR ML

-INFORMATION describe() METHOD GIVE ---   
count  -> NUMS OF NON-MISSING VALUES
mean   -> AVARAGE VALUE
std    -> STANDARD DEVIATION
min    -> SMALLEST VALUES
25%    -> FIRST QUARTILE [25% DATA BELOW]
50%    -> MEDIAN [MIDDLE VALUE]
75%    -> THIRD QUARTILE
max    -> LARGEST VALUE

-FOR ALL COLUMNS
"""

import pandas as pd

data = {
    "NAME":['RAJ', 'RAM', 'KUMAR', 'KUNAL', 'SHUBHAM', 'DHANRAJ', 'SAI', 'UDAV'],
    "AGE":[21, 20, 32, 18, 19, 34, 37, 54],
    "SALARY": [100000, 200000, 300000, 400000, 500000, 600000, 700000, 800000],
    "PERFORMANCE SCORE":[80,86,84,90,83,76,87,90]
}

df = pd.DataFrame(data)
print(df.describe())
